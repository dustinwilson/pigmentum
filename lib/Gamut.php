    public function addPrimary(Color ...$colors) {
        foreach ($colors as $c) {
            $LCH = $c->LCHab;

            $hue = $LCH->H - 40;
            $hue = ($hue > 0) ? $hue : 360 + $hue;

            // If the hue is close to a true primary location (every 60) then fudge it to
            // that location.
            $a = $hue / 60;
            $aa = $a - floor($a);

            if ($aa < 0.25) {
                $hue = floor($a) * 60;
            } elseif ($aa > 0.75) {
                $hue = ceil($a) * 60;
            } else {
                $hue = round($hue / 15) * 15;
            }

            $hue = ($hue != 360) ? $hue : 0;
            $this->_colors[$this->_positions[$hue]] = $c;
        }
    }

    protected function fillGaps() {
        $colors = array_values($this->_colors);
        $length = sizeof($colors);
        $gaps = [];
        $count = 0;
        $count2 = 0;
        $first = true;
        $primaries = array_filter($colors, function($a) use(&$gaps, &$count, &$count2, &$first) {
            if (is_null($a)) {
                $gaps[$count] = ++$count2;
                $first = false;
                return false;
            }

            $count++;

            if ($count2 === 0 && !$first) {
                $gaps[$count++] = 0;
            }

            $count2 = 0;
            $first = false;
            return true;
        });

        if (is_null($this->_neutral)) {
            $this->_neutral = call_user_func_array('\dW\Pigmentum\Color::average', $primaries);
        }

        $from = end($primaries);
        $to = reset($primaries);
        $gap = array_shift($gaps);
        $count = 0;
        foreach ($colors as $key => &$c) {
            if (!is_null($c)) {
                if ($count !== 0) {
                    $gap = array_shift($gaps);
                }

                $count = 0;
                if ($gap === 0) {
                    continue;
                }

                $toKey = $key + $gap + 1;
                if ($toKey > $length - 1) {
                    $toKey = $length - $toKey;
                }

                $to = $colors[$toKey];
                $from = $c;
                continue;
            }

            $c = $from->mix($to, ++$count / ($gap + 1));
        }

        $count = 0;
        foreach ($this->_colors as &$c) {
            $c = $colors[$count++];
        }
    }

    public function saveSVG(Color $center = null): string {
        if (is_null($this->_neutral)) {
            $this->fillGaps();
        }

        if (is_null($center)) {
            $center = $this->_neutral;
        }

        if (is_null($this->cache)) {
            $doc = new \DOMDocument();
            $doc->loadXML(self::$wheel);
            // Workaround so ids can be selected :/.
            $doc->relaxNGValidateSource(self::$relaxNG);
            $this->cache = $doc;
        } else {
            $doc = $this->cache;
        }

        $doc->getElementById('K5')->setAttribute('fill', $center->Hex);
        foreach ($this->_colors as $key => $c) {
            $doc->getElementById("{$key}11")->setAttribute('fill', $c->Hex);

            for($i = 1; $i < 11; $i++) {
                $doc->getElementById("{$key}{$i}")->setAttribute('fill', $center->mix($c, $i / 11)->Hex);
            }
        }

        return $doc->saveXML();
    }


